a = " Hello, World "

print(a)

# There are several methods to modify the string

# 1. To make the string upper case
print("Uppercase of string", a.upper())


# 2. To make the string lowercase
print("Lowercase of string", a.lower())


# 3. To clear all the whitespace from beginning and end
print("Without whitespace [on beginning and end] string", a.strip())


# 4. To replace any part of the string
print("Replacing LL with KP", a.replace("ll", "kp"))


# 5. To split the string
print("Splting the string on ,", a.split(","))
