# As we know there are several escape characters
# 1. \' Single Quote
print("Hello \'Everyone\'")

# 2. \" Double Quote
print("Hello \"Everyone\"")

# 3. \\ Backslash
print("Hello \\Everyone\\")

# 4. \n NewLine
print("\nHello\nWorld ")

# 5. \t Tab
print("Hello\tWorld")

print(7//2)
