# We use "=" inside multiple variables to assign same value
# to the multiple variables
x = y = z = "Orrange"

print(x)
print(y)
print(z)

#  We use "," inside multiple variables to unpack the list
fruits = ["Apple", "Mango", "grape"]
x, y, z = fruits

print(x)
print(y)
print(z)

x, y, z = "People", "Leader", "World"

print(x)
print(y)
print(z)

print(x, y, z)
